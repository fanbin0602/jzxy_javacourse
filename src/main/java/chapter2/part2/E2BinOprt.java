package chapter2.part2;

/**
 * 二元算数运算符
 * @author fanbin
 * @date 2019/9/2
 */
public class E2BinOprt {

    public static void main(String[] args) {
        int a = 5, b = 2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }

}
